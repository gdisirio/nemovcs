@dataclass(frozen=True)
class MenuActionSpec:
    name: str
    label: str
    command: tuple[str, ...] = ()
    tip: str = ""
    icon: str | None = MENU_ICON
    separator: bool = False
    sensitive: bool = True
    children: tuple["MenuActionSpec", ...] = ()


@dataclass(frozen=True)
class MenuGroupSpec:
    name: str
    label: str
    items: tuple[MenuActionSpec, ...]
    tip: str = ""
    icon: str = MENU_ICON


