class NemoVCSInfoProviderCore:
    def __init__(
        self,
        *,
        cache: status_client.StatusClientCache | None = None,
        seen=None,
        get_status=None,
        diagnostics=None,
        max_visible_items: int = DEFAULT_MAX_VISIBLE_ITEMS,
    ):
        if max_visible_items < 1:
            raise ValueError("max_visible_items must be at least 1")
        self.cache = cache if cache is not None else status_client.StatusClientCache()
        self.seen = seen
        self.get_status = get_status
        self.diagnostics = (
            diagnostics
            if diagnostics is not None
            else PluginDiagnostics.from_environment()
        )
        self.max_visible_items = max_visible_items
        self.visible_items: OrderedDict[str, object] = OrderedDict()
        self.last_error = ""

    def item_path(self, item) -> str | None:
        try:
            if item.get_uri_scheme() != "file":
                self.log("ignore-item", reason="non-file-uri")
                return None
            location = item.get_location()
            path = location.get_path()
        except Exception as exc:
            self.last_error = str(exc)
            self.log("item-error", error=self.last_error)
            return None

        return str(Path(path).resolve(strict=False)) if path else None

    def update_item(self, item) -> dict[str, str] | None:
        path = self.item_path(item)
        if path is None:
            return None
        self.track_visible_item(path, item)
        record = self.update_path(path)
        emblem = self.apply_emblem(item, record)
        self.log(
            "update-item",
            path=path,
            status=record.get("status", "") if record else "",
            emblem=emblem or "",
        )
        return record

    def update_path(self, path: str | Path) -> dict[str, str] | None:
        cached = self.cache.get(path)
        if cached is not None:
            self.log(
                "status-cache-hit",
                path=str(path),
                status=cached.get("status", ""),
                worktree_id=cached.get("worktree_id", ""),
            )
            return cached

        seen = self.seen or default_seen
        get_status = self.get_status or default_get_status
        for attempt in range(1, 3):
            try:
                records = self.cache.refresh([path], seen, get_status)
                break
            except Exception as exc:
                self.last_error = str(exc)
                self.log(
                    "status-error",
                    path=str(path),
                    attempt=attempt,
                    error=self.last_error,
                )
                if attempt == 2:
                    return None

        self.last_error = ""
        record = records[0] if records else None
        self.log(
            "status",
            path=str(path),
            status=record.get("status", "") if record else "",
            worktree_id=record.get("worktree_id", "") if record else "",
        )
        return record

    def apply_emblem(self, item, record: dict[str, str] | None) -> str | None:
        if not record:
            return None

        emblem = primary_emblem(record.get("status", ""))
        if emblem is None:
            return None

        try:
            item.add_emblem(emblem)
        except Exception as exc:
            self.last_error = str(exc)
            self.log("emblem-error", emblem=emblem, error=self.last_error)
            return None

        return emblem

    def track_visible_item(self, path: str | Path, item) -> None:
        key = status_client.normalize_path(path)
        if key in self.visible_items:
            del self.visible_items[key]
        self.visible_items[key] = item
        self.visible_items.move_to_end(key, last=False)
        while len(self.visible_items) > self.max_visible_items:
            self.visible_items.popitem(last=True)

    def on_status_changed(
        self,
        worktree_id: str,
        changed_paths,
    ) -> list[str]:
        affected_paths = self.affected_visible_paths(worktree_id, changed_paths)
        removed = self.cache.invalidate(worktree_id, changed_paths)
        invalidated = [
            path
            for path in affected_paths
            if self.invalidate_visible_item(path)
        ]
        self.log(
            "status-changed",
            worktree_id=str(worktree_id),
            changed_count=len(changed_paths),
            affected_count=len(affected_paths),
            removed_count=len(removed),
            invalidated_count=len(invalidated),
        )
        return invalidated

    def affected_visible_paths(self, worktree_id: str, changed_paths) -> list[str]:
        changed = [status_client.normalize_path(path) for path in changed_paths]
        affected: list[str] = []
        for path in self.visible_items:
            record = self.cache.get(path)
            if record is None or record.get("worktree_id") != str(worktree_id):
                continue
            if not changed or any(
                status_client.paths_overlap(path, changed_path)
                for changed_path in changed
            ):
                affected.append(path)
        return affected

    def invalidate_visible_item(self, path: str | Path) -> bool:
        item = self.visible_items.get(status_client.normalize_path(path))
        if item is None:
            return False

        invalidate = getattr(item, "invalidate_extension_info", None)
        if invalidate is None:
            return False

        try:
            invalidate()
        except Exception as exc:
            self.last_error = str(exc)
            self.log("invalidate-error", path=str(path), error=self.last_error)
            return False
        return True

    def log(self, event: str, **fields) -> None:
        if self.diagnostics is not None:
            self.diagnostics.log(event, **fields)

    def menu_paths(self, items) -> list[str]:
        paths: list[str] = []
        for item in items:
            path = self.item_path(item)
            if path is not None:
                paths.append(path)
        return paths

    def submenu_groups(self, paths: Sequence[str | Path]) -> list[MenuGroupSpec]:
        normalized = [str(Path(path).resolve(strict=False)) for path in paths]
        if not normalized:
            return []

        if all(is_clone_target(path) for path in normalized):
            return [
                MenuGroupSpec(
                    name="NemoVCS::GitMenu",
                    label="Git NemoVCS",
                    tip="Git actions",
                    icon=GIT_ICON,
                    items=tuple(git_clone_menu_specs(normalized)),
                ),
                MenuGroupSpec(
                    name="NemoVCS::SvnMenu",
                    label="SVN NemoVCS",
                    tip="Subversion actions",
                    icon=SVN_ICON,
                    items=tuple(svn_checkout_menu_specs(normalized)),
                ),
            ]

        groups: list[MenuGroupSpec] = []
        for backend_id in matching_backend_ids(normalized):
            if backend_id == "git":
                groups.append(
                    MenuGroupSpec(
                        name="NemoVCS::GitMenu",
                        label="Git NemoVCS",
                        tip="Git actions",
                        icon=GIT_ICON,
                        items=tuple(git_menu_specs(normalized)),
                    )
                )
            elif backend_id == "svn":
                groups.append(
                    MenuGroupSpec(
                        name="NemoVCS::SvnMenu",
                        label="SVN NemoVCS",
                        tip="Subversion actions",
                        icon=SVN_ICON,
                        items=tuple(svn_menu_specs(normalized)),
                    )
                )
        return groups

    def top_level_specs(self, paths: Sequence[str | Path]) -> list[MenuActionSpec]:
        normalized = [str(Path(path).resolve(strict=False)) for path in paths]
        if not normalized:
            return []

        compare_spec = two_path_compare_spec(normalized)
        if compare_spec is not None:
            return [compare_spec]

        if all(is_clone_target(path) for path in normalized):
            return []

        if matching_backend_ids(normalized) and self.diff_paths_visible(normalized):
            return common_top_level_specs(normalized)
        return []

    def diff_paths_visible(self, paths: Sequence[str]) -> bool:
        if not paths:
            return False
        for path in paths:
            record = self.cache.get(path)
            if record is None:
                record = self.update_path(path)
            if record is None or record.get("status", "") not in DIFFABLE_STATUSES:
                return False
        return True

    def location_widget_spec(self, path: str | Path) -> LocationWidgetSpec | None:
        record = self.update_path(path)
        if record is None:
            return None

        backend = str(record.get("backend", ""))
        root = str(record.get("root", ""))
        if not backend or not root:
            return None

        head = str(record.get("head", "")) or "unknown"
        status = str(record.get("status", ""))
        return LocationWidgetSpec(
            backend=backend,
            backend_label=BACKEND_LABELS.get(backend, backend.upper()),
            head=head,
            status=status,
            status_label=STATUS_LABELS.get(status, status or "unknown"),
            root=root,
            root_label=Path(root).name or root,
            icon=BACKEND_ICONS.get(backend, MENU_ICON),
            remote=str(record.get("remote", "")),
        )


class PluginDiagnostics:
    def __init__(self, path: str | Path):
        self.path = Path(path).expanduser()

    @classmethod
    def from_environment(cls):
        path = os.environ.get("NEMOVCS_PLUGIN_LOG")
        return cls(path) if path else None

    def log(self, event: str, **fields) -> None:
        record = {"time": time.time(), "event": event, **fields}
        try:
            self.path.parent.mkdir(parents=True, exist_ok=True)
            with self.path.open("a", encoding="utf-8") as stream:
                stream.write(json.dumps(record, sort_keys=True) + "\n")
        except OSError:
            pass


class NemoVCSInfoProviderMixin:
    def __init__(self):
        self.nemovcs_core = NemoVCSInfoProviderCore()
        self.nemovcs_signal_subscription = None
        self.subscribe_status_changed()
        self.nemovcs_core.log("provider-init")

    def update_file_info(self, item):
        from gi.repository import Nemo

        self.nemovcs_core.update_item(item)
        return Nemo.OperationResult.COMPLETE

    def get_file_items(self, _window, items):
        paths = self.nemovcs_core.menu_paths(items)
        return self.nemovcs_menu_items(paths)

    def get_background_items(self, _window, item):
        path = self.nemovcs_core.item_path(item)
        return self.nemovcs_menu_items([path] if path else [])

    def get_widget(self, uri, _window):
        path = path_from_uri(uri)
        if path is None:
            return None

        spec = self.nemovcs_core.location_widget_spec(path)
        if spec is None:
            return None
        return self.nemovcs_location_widget(spec)

    def nemovcs_menu_items(self, paths: Sequence[str]) -> list[object]:
        top_level_specs = self.nemovcs_core.top_level_specs(paths)
        submenu_groups = self.nemovcs_core.submenu_groups(paths)
        if not top_level_specs and not submenu_groups:
            return []

        from gi.repository import Nemo

        items = [
            self.nemovcs_menu_item(Nemo, spec)
            for spec in top_level_specs
            if not spec.separator
        ]

        for group in submenu_groups:
            items.append(self.nemovcs_submenu_item(Nemo, group))
        items.append(Nemo.MenuItem.new_separator("NemoVCS::AfterMenu"))
        return items

    def nemovcs_submenu_item(self, Nemo, group: MenuGroupSpec):
        submenu_item = Nemo.MenuItem(
            name=group.name,
            label=group.label,
            tip=group.tip,
            icon=group.icon,
        )
        submenu = Nemo.Menu()
        submenu_item.set_submenu(submenu)
        self.nemovcs_append_menu_specs(Nemo, submenu, group.items)
        return submenu_item

    def nemovcs_append_menu_specs(self, Nemo, menu, specs: Sequence[MenuActionSpec]):
        for spec in specs:
            if spec.separator:
                menu.append_item(Nemo.MenuItem.new_separator(spec.name))
                continue
            menu.append_item(self.nemovcs_menu_item(Nemo, spec))

    def nemovcs_menu_item(self, Nemo, spec: MenuActionSpec):
        item = Nemo.MenuItem(
            name=spec.name,
            label=spec.label,
            tip=spec.tip,
            icon=spec.icon,
        )
        if spec.children:
            submenu = Nemo.Menu()
            item.set_submenu(submenu)
            self.nemovcs_append_menu_specs(Nemo, submenu, spec.children)
        elif spec.command:
            item.connect("activate", self.on_menu_item_activate, spec.command)
        if not spec.sensitive:
            set_sensitive = getattr(item, "set_sensitive", None)
            if set_sensitive is not None:
                set_sensitive(False)
            else:
                item.set_property("sensitive", False)
        return item

    def nemovcs_location_widget(self, spec: LocationWidgetSpec):
        from gi.repository import Gtk, Pango

        outer = Gtk.Box(orientation=Gtk.Orientation.VERTICAL, spacing=4)
        outer.set_border_width(4)
        outer.set_hexpand(True)
        outer.set_tooltip_text(location_widget_tooltip(spec))

        box = Gtk.Box(orientation=Gtk.Orientation.HORIZONTAL, spacing=8)
        box.set_hexpand(True)
        outer.pack_start(box, False, False, 0)

        image = Gtk.Image.new_from_icon_name(spec.icon, Gtk.IconSize.MENU)
        box.pack_start(image, False, False, 0)

        backend = Gtk.Label()
        backend.set_markup(f"<b>{escape(spec.backend_label)}</b>")
        backend.set_xalign(0)
        box.pack_start(backend, False, False, 0)

        head = Gtk.Label(label=compact_text(spec.head))
        head.set_ellipsize(Pango.EllipsizeMode.END)
        head.set_max_width_chars(LOCATION_BAR_MAX_CHARS)
        head.set_xalign(0)
        head.set_selectable(True)
        box.pack_start(head, False, False, 0)

        status = Gtk.Label(label=f"- {spec.status_label}")
        status.set_xalign(0)
        box.pack_start(status, False, False, 0)

        root = Gtk.Label(label=location_widget_root_label(spec, expanded=False))
        root.set_ellipsize(Pango.EllipsizeMode.END)
        root.set_max_width_chars(LOCATION_BAR_MAX_CHARS)
        root.set_hexpand(True)
        root.set_xalign(0)
        root.set_selectable(True)
        box.pack_start(root, True, True, 0)

        details = self.nemovcs_location_details_widget(Gtk, spec)
        outer.pack_start(details, False, False, 0)

        toggle = Gtk.ToggleButton()
        toggle.set_relief(Gtk.ReliefStyle.NONE)
        toggle.set_tooltip_text("Show repository details")
        toggle.set_image(
            Gtk.Image.new_from_icon_name("pan-down-symbolic", Gtk.IconSize.MENU)
        )
        toggle.connect(
            "toggled",
            self.on_location_details_toggled,
            details,
            head,
            status,
            root,
            spec,
        )
        box.pack_start(toggle, False, False, 0)

        separator = Gtk.Separator(orientation=Gtk.Orientation.HORIZONTAL)
        outer.pack_start(separator, False, False, 0)

        outer.show_all()
        details.hide()
        return outer

    def nemovcs_location_details_widget(self, Gtk, spec: LocationWidgetSpec):
        grid = Gtk.Grid(column_spacing=10, row_spacing=2)
        grid.set_margin_start(24)

        for row, (label_text, value_text) in enumerate(location_widget_details(spec)):
            label = Gtk.Label()
            label.set_markup(f"<b>{escape(label_text)}</b>")
            label.set_xalign(0)
            grid.attach(label, 0, row, 1, 1)

            value = Gtk.Label(label=value_text)
            value.set_xalign(0)
            value.set_selectable(True)
            value.set_line_wrap(True)
            grid.attach(value, 1, row, 1, 1)

        return grid

    def on_location_details_toggled(
        self,
        button,
        details,
        head,
        status,
        root,
        spec,
    ) -> None:
        from gi.repository import Gtk

        expanded = button.get_active()
        if expanded:
            details.show_all()
            head.hide()
            status.hide()
            root.set_text(location_widget_root_label(spec, expanded=True))
            root.set_max_width_chars(80)
        else:
            details.hide()
            head.show()
            status.show()
            root.set_text(location_widget_root_label(spec, expanded=False))
            root.set_max_width_chars(LOCATION_BAR_MAX_CHARS)
        details.get_parent().queue_resize()
        button.set_tooltip_text(
            "Hide repository details" if expanded else "Show repository details"
        )
        icon = "pan-up-symbolic" if expanded else "pan-down-symbolic"
        button.set_image(Gtk.Image.new_from_icon_name(icon, Gtk.IconSize.MENU))

    def on_menu_item_activate(self, _item, command: Sequence[str]) -> None:
        launch_command = menu_launch_command(command)
        try:
            subprocess.Popen(launch_command, env=menu_launch_env(command))
        except OSError as exc:
            self.nemovcs_core.last_error = str(exc)
            self.nemovcs_core.log(
                "menu-spawn-error",
                command=" ".join(launch_command),
                error=str(exc),
            )

    def subscribe_status_changed(self) -> bool:
        try:
            self.nemovcs_signal_subscription = subscribe_daemon_status_changed(
                self.nemovcs_core.on_status_changed
            )
        except Exception as exc:
            self.nemovcs_core.last_error = str(exc)
            return False
        return True


def default_seen(paths):
    from . import statusd_dbus

    return statusd_dbus.call_seen(paths)


def path_from_uri(uri: str) -> str | None:
    parsed = urlparse(uri)
    if parsed.scheme and parsed.scheme != "file":
        return None
    path = unquote(parsed.path if parsed.scheme else uri)
    return str(Path(path).resolve(strict=False)) if path else None


def compact_text(text: str, *, max_chars: int = LOCATION_BAR_MAX_CHARS) -> str:
    if len(text) <= max_chars:
        return text
    if max_chars <= 1:
        return text[:max_chars]
    return text[: max_chars - 1] + "..."


def location_widget_details(spec: LocationWidgetSpec) -> list[tuple[str, str]]:
    details: list[tuple[str, str]] = []
    if spec.remote:
        details.append(("Remote", spec.remote))
    details.append(("Worktree", spec.root))
    details.extend(
        [
            ("Head", spec.head),
            ("Status", spec.status_label),
            ("Backend", spec.backend_label),
        ]
    )
    return details


def location_widget_tooltip(spec: LocationWidgetSpec) -> str:
    return "\n".join(
        f"{label}: {value}" for label, value in location_widget_details(spec)
    )


def location_widget_root_label(
    spec: LocationWidgetSpec,
    *,
    expanded: bool,
) -> str:
    if expanded:
        return spec.remote or spec.root
    return f"- {compact_text(spec.root_label)}"


def default_get_status(paths):
    from . import statusd_dbus

    return statusd_dbus.call_get_status(paths)


def subscribe_daemon_status_changed(callback):
    import dbus.mainloop.glib

    from . import statusd_dbus

    dbus.mainloop.glib.DBusGMainLoop(set_as_default=True)
    return statusd_dbus.subscribe_status_changed(callback)


def menu_launch_command(command: Sequence[str]) -> list[str]:
    if command and command[0] == "nemovcs":
        return [sys.executable, "-m", *command]
    return list(command)


def menu_launch_env(command: Sequence[str]) -> dict[str, str] | None:
    if not command or command[0] != "nemovcs":
        return None

    source_root = str(Path(__file__).resolve().parents[1])
    env = os.environ.copy()
    current = env.get("PYTHONPATH")
    env["PYTHONPATH"] = source_root if not current else f"{source_root}:{current}"
    return env


def primary_emblem(status: str) -> str | None:
    return PRIMARY_EMBLEMS.get(status)


def matching_backend_ids(paths: Sequence[str | Path]) -> list[str]:
    from . import backends

    ids: list[str] = []
    for backend in backends.registered_backends():
        if all(backend.is_worktree(path) for path in paths):
            ids.append(backend.id)
    return ids


def is_clone_target(path: str | Path) -> bool:
    from . import backends

    candidate = Path(path).expanduser()
    if not candidate.is_absolute():
        candidate = Path.cwd() / candidate
    candidate = candidate.resolve(strict=False)
    return candidate.is_dir() and backends.detect_backend(candidate) is None


def git_menu_specs(paths: Sequence[str]) -> list[MenuActionSpec]:
    specs = [
        action("GitCommit", "Commit...", ["commit-dialog", *paths], icon=COMMIT_ICON),
        action("GitUpdate", "Update...", ["update-dialog", *paths], icon=UPDATE_ICON),
        separator("GitSep0"),
        action(
            "GitStage",
            "Stage...",
            ["stage-dialog", "--operation", "stage", *paths],
            icon=ADD_ICON,
        ),
    ]
    if len(paths) == 1:
        specs.append(
            action("GitRename", "Rename...", ["rename-dialog", *paths], icon=RENAME_ICON)
        )
        switch_spec = git_switch_branch_menu_spec(paths[0])
        if switch_spec is not None:
            specs.append(switch_spec)
    specs.extend(
        [
            action(
                "GitRevert",
                "Revert...",
                ["revert-dialog", *paths],
                icon=REVERT_ICON,
            ),
            action("GitPush", "Push...", ["push-dialog", *paths], icon=PUSH_ICON),
            separator("GitSep1"),
            action("GitStatus", "Status...", ["status-dialog", *paths], icon=STATUS_ICON),
            action("GitLog", "Log...", ["log-dialog", *paths], icon=LOG_ICON),
            separator("GitSep2"),
            action("GitSettings", "Settings...", ["settings-dialog"], icon=SETTINGS_ICON),
            action("GitAbout", "About...", ["about-dialog"], icon=ABOUT_ICON),
        ]
    )
    return specs


def git_switch_branch_menu_spec(path: str) -> MenuActionSpec | None:
    from . import git

    root = git.repo_root(path)
    if root is None:
        return None

    current = git.current_branch_name(root)
    dirty = git.worktree_dirty(root)
    branches = git.recent_branches(root)
    if current and current not in branches:
        branches.insert(0, current)
    branch_locations = git.worktree_branch_locations(root)

    children: list[MenuActionSpec] = []
    for branch in branches:
        if branch == current:
            children.append(
                MenuActionSpec(
                    name=f"NemoVCS::GitSwitchCurrent::{menu_name_fragment(branch)}",
                    label=branch,
                    tip="Current branch",
                    icon="object-select-symbolic",
                    sensitive=False,
                )
            )
            continue
        branch_location = branch_locations.get(branch)
        checked_out_elsewhere = (
            branch_location is not None
            and branch_location.resolve(strict=False) != root.resolve(strict=False)
        )
        branch_tip = branch
        if dirty:
            branch_tip = "Working tree has changes"
        elif checked_out_elsewhere:
            branch_tip = f"Checked out at {branch_location}"
        children.append(
            MenuActionSpec(
                name=f"NemoVCS::GitSwitchBranch::{menu_name_fragment(branch)}",
                label=branch,
                command=("nemovcs", "switch-branch-dialog", str(root), branch),
                tip=branch_tip,
                icon=None,
                sensitive=not dirty and not checked_out_elsewhere,
            )
        )

    if children:
        children.append(separator("GitSwitchBranchSep"))
    children.append(
        MenuActionSpec(
            name="NemoVCS::GitSwitchBranchOthers",
            label="Others...",
            command=("nemovcs", "switch-branch-dialog", str(root)),
            tip="Working tree has changes" if dirty else "Show all branches",
            icon=None,
            sensitive=not dirty,
        )
    )

    return MenuActionSpec(
        name="NemoVCS::GitSwitchBranch",
        label="Switch Branch",
        tip="Working tree has changes" if dirty else "Switch Git branch",
        icon=MODIFIED_MENU_ICON if dirty else GIT_ICON,
        children=tuple(children),
    )


